// operadores de asignacion explicacion y ejeplo completos
public class OpAsignacion {
    public static void main(String[] args) {
        int num1 = 10, num2 = 16;
        num1 += num2;
        System.out.println("Suma: " + num1);
        num1 -= num2;
        System.out.println("Resta: " + num1);
        num1 *= num2;
        System.out.println("Multiplicacion: " + num1);
        num1 /= num2;
        System.out.println("Division: " + num1);
        num1 %= num2;
        System.out.println("Modulo: " + num1);
    }
}
